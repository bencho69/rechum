<p style='font-size:8.0pt; text-align:center; line-height: 0.9;line-height: 0.95;'><span >A N T E C E D E N T E S:</span></p>


<p style='font-size:7.0pt;text-align:justify; text-justify: auto|inter-word;line-height: 0.95;'><span >En el marco
del denominado Sistema de Protección Social en Salud (SPSS), creado por las
modificaciones realizadas a la Ley General de Salud, aprobadas por el H.
Congreso de la Unión, publicadas en el Diario Oficial de la Federación el 15 de
Mayo del 2003, así como las modificaciones a su reglamento publicado el 5 de
Abril del 2014, los Gobiernos Federal y Estatal crean el instrumento financiero
mediante el cual se establece a nivel de Ley la posibilidad de operar el Seguro
Popular para ofrecer a todas las familias que no sean derechohabientes de las
instituciones de seguridad social o no cuentan con algún mecanismo de Previsión
Social en Salud, sin importar su condición social y sin discriminación,
Servicios Médicos Quirúrgicos, Farmacéuticos y Hospitalarios integrados en el
Catálogo Universal de Servicios de Salud (CAUSES), sin necesidad de pago o
desembolso económico en el momento de recibir las acciones incluidas, la cuales
hasta el momento son un total de 287 intervenciones y que serán otorgadas por
medio de la red de servicios de salud con que cuenta la Secretaría de Salud y
están incluidas en la red de servicios del Sistema Estatal de Protección Social
de Salud. Para lograr lo antes expuesto, se crea el Régimen Estatal de
Protección Social en Salud en Guerrero (REPSSG), como Organismo Público
Descentralizado, con personalidad jurídica y patrimonio propio, con el objeto
de garantizar las acciones de protección social en salud en la entidad para la
provisión de los servicios de salud, teniendo entre sus atribuciones organizar,
administrar y operar en Guerrero el Sistema Estatal de Protección Social en
Salud y por consecuencia la contratación de personal profesional y de apoyo
técnico y administrativo indispensable para el cumplimiento del objeto de dicho
Régimen.</span></p>

